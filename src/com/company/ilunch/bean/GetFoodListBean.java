package com.company.ilunch.bean;

import java.util.ArrayList;
import com.alibaba.fastjson.annotation.JSONField;

/**
 * 菜品列表接口返回信息JavaBean
 */
public class GetFoodListBean {
	@JSONField(name = "Body")
	private ArrayList<Body> body;
	@JSONField(name = "Head")
	private Head head;

	public ArrayList<Body> getBody() {
		return body;
	}

	public void setBody(ArrayList<Body> body) {
		this.body = body;
	}

	public Head getHead() {
		return head;
	}

	public void setHead(Head head) {
		this.head = head;
	}

	public static class Head {
		@JSONField(name = "ResultCode")
		private String resultCode;
		@JSONField(name = "ResultInfo")
		private String resultInfo;

		public String getResultCode() {
			return resultCode;
		}

		public void setResultCode(String resultCode) {
			this.resultCode = resultCode;
		}

		public String getResultInfo() {
			return resultInfo;
		}

		public void setResultInfo(String resultInfo) {
			this.resultInfo = resultInfo;
		}
	}

	public static class Body {
		@JSONField(name = "Unid")
		private String unid;
		@JSONField(name = "InUse")
		private String inUse;
		@JSONField(name = "FoodName")
		private String foodName;
		@JSONField(name = "FPrice")
		private String price;
		@JSONField(name = "FullPrice")
		private String fullPrice;
		@JSONField(name = "Picture")
		private String picture;
		@JSONField(name = "FPInDate")
		private String inDate;
		@JSONField(name = "FPMaster")
		private String master;
		@JSONField(name = "FPName")
		private String name;
		@JSONField(name = "commentNum")
		private String commentNum;
		@JSONField(name = "isCollected")
		private String isCollected;
		@JSONField(name = "Remains")
		private String remains;

		public String getUnid() {
			return unid;
		}

		public void setUnid(String unid) {
			this.unid = unid;
		}

		public String getInUse() {
			return inUse;
		}

		public void setInUse(String inUse) {
			this.inUse = inUse;
		}

		public String getFoodName() {
			return foodName;
		}

		public void setFoodName(String foodName) {
			this.foodName = foodName;
		}

		public String getPrice() {
			return price;
		}

		public void setPrice(String price) {
			this.price = price;
		}

		public String getFullPrice() {
			return fullPrice;
		}

		public void setFullPrice(String fullPrice) {
			this.fullPrice = fullPrice;
		}

		public String getPicture() {
			return picture;
		}

		public void setPicture(String picture) {
			this.picture = picture;
		}

		public String getInDate() {
			return inDate;
		}

		public void setInDate(String inDate) {
			this.inDate = inDate;
		}

		public String getMaster() {
			return master;
		}

		public void setMaster(String master) {
			this.master = master;
		}

		public String getName() {
			return name;
		}

		public void setName(String name) {
			this.name = name;
		}

		public String getCommentNum() {
			return commentNum;
		}

		public void setCommentNum(String commentNum) {
			this.commentNum = commentNum;
		}

		public String getIsCollected() {
			return isCollected;
		}

		public void setIsCollected(String isCollected) {
			this.isCollected = isCollected;
		}

		public String getRemains() {
			return remains;
		}

		public void setRemains(String remains) {
			this.remains = remains;
		}
	}
}
