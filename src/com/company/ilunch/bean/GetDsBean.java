package com.company.ilunch.bean;

import java.util.ArrayList;
import com.alibaba.fastjson.annotation.JSONField;

/**
 * 大厦列表返回信息JavaBean
 */
public class GetDsBean {
	@JSONField(name = "Body")
	private ArrayList<Body> body;
	@JSONField(name = "Head")
	private Head head;

	public ArrayList<Body> getBody() {
		return body;
	}

	public void setBody(ArrayList<Body> body) {
		this.body = body;
	}

	public Head getHead() {
		return head;
	}

	public void setHead(Head head) {
		this.head = head;
	}

	public static class Head {
		@JSONField(name = "ResultCode")
		private String resultCode;
		@JSONField(name = "ResultInfo")
		private String resultInfo;

		public String getResultCode() {
			return resultCode;
		}

		public void setResultCode(String resultCode) {
			this.resultCode = resultCode;
		}

		public String getResultInfo() {
			return resultInfo;
		}

		public void setResultInfo(String resultInfo) {
			this.resultInfo = resultInfo;
		}
	}

	public static class Body {
		@JSONField(name = "DataID")
		private String dataID;
		@JSONField(name = "Name")
		private String name;
		@JSONField(name = "Address")
		private String address;
		@JSONField(name = "Type")
		private String type;
		@JSONField(name = "Picture")
		private String picture;
		@JSONField(name = "Remark")
		private String remark;
		@JSONField(name = "SectionID")
		private String sectionID;
		@JSONField(name = "FirstL")
		private String firstL;
		@JSONField(name = "SectionName")
		private String sectionName;
		@JSONField(name = "XYUrl")
		private String xYUrl;
		@JSONField(name = "Lat")
		private String lat;
		@JSONField(name = "Lng")
		private String lng;
		@JSONField(name = "isShow")
		private String isShow;
		
		public String getDataID() {
			return dataID;
		}
		public void setDataID(String dataID) {
			this.dataID = dataID;
		}
		public String getName() {
			return name;
		}
		public void setName(String name) {
			this.name = name;
		}
		public String getAddress() {
			return address;
		}
		public void setAddress(String address) {
			this.address = address;
		}
		public String getType() {
			return type;
		}
		public void setType(String type) {
			this.type = type;
		}
		public String getPicture() {
			return picture;
		}
		public void setPicture(String picture) {
			this.picture = picture;
		}
		public String getRemark() {
			return remark;
		}
		public void setRemark(String remark) {
			this.remark = remark;
		}
		public String getSectionID() {
			return sectionID;
		}
		public void setSectionID(String sectionID) {
			this.sectionID = sectionID;
		}
		public String getFirstL() {
			return firstL;
		}
		public void setFirstL(String firstL) {
			this.firstL = firstL;
		}
		public String getSectionName() {
			return sectionName;
		}
		public void setSectionName(String sectionName) {
			this.sectionName = sectionName;
		}
		public String getxYUrl() {
			return xYUrl;
		}
		public void setxYUrl(String xYUrl) {
			this.xYUrl = xYUrl;
		}
		public String getLat() {
			return lat;
		}
		public void setLat(String lat) {
			this.lat = lat;
		}
		public String getLng() {
			return lng;
		}
		public void setLng(String lng) {
			this.lng = lng;
		}
		public String getIsShow() {
			return isShow;
		}
		public void setIsShow(String isShow) {
			this.isShow = isShow;
		}
	}
}
